//  Boolean value.
let javascriptIsfun = true;
console.log(javascriptIsfun);
// Type of.
console.log(typeof javascriptIsfun);

// Dynamic typing.

javascriptIsfun = "Yes!";
console.log(typeof javascriptIsfun);

// Undefined value.
let year;
console.log(year);
console.log(typeof year);

// Const value.

const birthYear = 1999;
// birthYear = 2000;
console.log(birthYear);

